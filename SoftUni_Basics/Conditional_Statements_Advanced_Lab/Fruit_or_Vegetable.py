snack = input()

if snack == "banana" or snack == "apple" or snack == "kiwi" or snack == "cherry" or snack == "lemon" or snack == "grapes":
    print("fruit")
elif snack == "tomato" or snack == "cucumber" or snack == "pepper" or snack == "carrot":
    print("vegetable")
else:
    print("unknown")
